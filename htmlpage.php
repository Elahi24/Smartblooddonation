 <h3>রক্তের মূল অংশ দুইটি। যথা:</h3>
 <h4>রক্তরস (Blood Plasma)</h4>
 <h4>রক্ত কণিকা (Blood corpuscle)</h4>

 <h3>রক্তরস</h3>
 <h4>রক্তের তরল, হালকা হলুদাভ অংশকে রক্তরস (plasma) বলে। রক্তকণিকা ব্যতীত রক্তের বাকি অংশই রক্ত রস। মেরুদন্ডী প্রাণিদের
  রক্তের প্রায় ৫৫% রক্তরস।</h4>

 <h3>রক্তকণিকা</h3>
 <h4>রক্তের প্লাজমার মধ্যে নির্দিষ্ট আকার ও গঠন বিশিষ্ট উপাদান বা রক্ত কোষসমূহকে রক্ত কণিকা বলে। রক্তে প্রায় তিন ধরনের
  কণিকা পাওয়া যায়। যথা:</h4>
 <h4>লোহিত রক্তকণিকা (Erythorcytes)</h4>
 <h4> শ্বেত রক্তকণিকা (Leucocytes) </h4>
 <h4> অণুচক্রিকা (Thrombocytes)</h4>

 <h3>রক্তদান</h3>
 <h4>রক্তদান কতটা নিরাপদ এটা নিয়ে কারো কারো মধ্যে সংশয় আছে। রক্তদানের কোনো ঝুঁকি নেই। একজন পুরুষের শরীরে ওজনের কেজিপ্রতি
  ৭৬ মিলিলিটার এবং নারীদের শরীরে ওজনের কেজিপ্রতি ৬৬ মিলিলিটার রক্ত থাকে। সবারই কেজিপ্রতি ৫০ মিলিলিটার রক্ত সংবহনের কাজে
  লাগে, বাকিটা উদ্বৃত্ত। অর্থাৎ পুরুষের ওজনের কেজিপ্রতি ২৬ মিলিলিটার ও নারীদের ওজনের কেজিপ্রতি ১৬ মিলিলিটার রক্ত
  উদ্বৃত্ত। ফলে ৫০ কেজি ওজনের একজন পুরুষের উদ্বৃত্ত রক্তের পরিমাণ 50×26=1300 মিলি এবং একই ওজনের একজন নারীর শরীরে
  উদ্বৃত্ত রক্তের পরিমাণ 50×16=800 মিলি। স্বেচ্ছা রক্তদানে একজন দাতার কাছ থেকে ৩৫০ থেকে ৪০০ মিলিলিটার রক্ত সংগ্রহ করা
  হয়, যা তার শরীরের উদ্বৃত্ত রক্তের অর্ধেকেরও কম। ফলে রক্তদানে শারীরিক ক্ষতি হওয়ার কোনো আশঙ্কাই নেই।

